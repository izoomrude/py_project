
def funk_for_sum(num_1, num_2):
    num_1 = int(input('Введите первое число: '))
    num_2 = int(input('Введите второе число: '))
    new_num = num_1 + num_2
    return print(f'Сумма чисел: {new_num}')


def funk_for_subtraction():
    num_1 = int(input('Введите первое число: '))
    num_2 = int(input('Введите второе число: '))
    new_num = num_1 - num_2
    return print(f'Разница чисел: {new_num}')
